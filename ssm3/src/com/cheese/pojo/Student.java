package com.cheese.pojo;

import java.util.Date;

public class Student {
    private Integer stuid;

    private String stuname;

    private String sex;

    private Integer idnumber;

    private String phone;

    private Date regtime;

    private Date vailtime;

    private Short bbnumber;

    private String stustate;

	public Integer getStuid() {
		return stuid;
	}

	public void setStuid(Integer stuid) {
		this.stuid = stuid;
	}

	public String getStuname() {
		return stuname;
	}

	public void setStuname(String stuname) {
		this.stuname = stuname;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public Integer getIdnumber() {
		return idnumber;
	}

	public void setIdnumber(Integer idnumber) {
		this.idnumber = idnumber;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public Date getRegtime() {
		return regtime;
	}

	public void setRegtime(Date regtime) {
		this.regtime = regtime;
	}

	public Date getVailtime() {
		return vailtime;
	}

	public void setVailtime(Date vailtime) {
		this.vailtime = vailtime;
	}

	public Short getBbnumber() {
		return bbnumber;
	}

	public void setBbnumber(Short bbnumber) {
		this.bbnumber = bbnumber;
	}

	public String getStustate() {
		return stustate;
	}

	public void setStustate(String stustate) {
		this.stustate = stustate;
	}

}
